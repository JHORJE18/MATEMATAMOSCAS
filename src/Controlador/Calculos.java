package Controlador;

public class Calculos {
	
	public int sumar(int num1, int num2){
		int resto = num1 + num2;
		return resto;
	}
	
	public int restar(int num1, int num2){
		int resto = num1 - num2;
		return resto;
	}
	
	public int multiplicar(int num1, int num2){
		int resto = num1 * num2;
		return resto;
	}
	
	public int dividir(int num1, int num2){
		int resto = num1 / num2;
		return resto;
	}

}
