package Datos;

public class DatoAjustes {
	
	int rAlto = 320;
	int rAncho = 500;
	int vSonido = 100;
	int vMusica = 100;
	int nNivel = 1;
	
	//Getters & Seenders
	
	public int getrAlto() {
		return rAlto;
	}
	public void setrAlto(int rAlto) {
		this.rAlto = rAlto;
	}
	public int getrAncho() {
		return rAncho;
	}
	public void setrAncho(int rAncho) {
		this.rAncho = rAncho;
	}
	public int getvSonido() {
		return vSonido;
	}
	public void setvSonido(int vSonido) {
		this.vSonido = vSonido;
	}
	public int getvMusica() {
		return vMusica;
	}
	public void setvMusica(int vMusica) {
		this.vMusica = vMusica;
	}
	public int getnNivel() {
		return nNivel;
	}
	public void setnNivel(int nNivel) {
		this.nNivel = nNivel;
	}
	
}
